
//creating the world
const Engine = Matter.Engine;
const World = Matter.World;
const Bodies = Matter.Bodies;

var engine,world;
var b,ball;

function setup() {
  
  createCanvas(800,400);
  
  engine= Engine.create();
  world= engine.world;
  var o= {isStatic: true}
  b= Bodies.rectangle(400,380,800,10, o);
  World.add(world, b);
    

  var ball_o= {restitution : 0.8}
  ball= Bodies.circle(400,80,10, ball_o);
  World.add(world, ball);
  console.log(b);

  //createSprite(400, 200, 50, 50);
}

function draw() {
  background("lime");
  Engine.update(engine);
 
  rectMode(CENTER);
  rect(b.position.x, b.position.y,800,10); 

  ellipseMode(RADIUS);
  ellipse(ball.position.x, ball.position.y, 10,10);
}


































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































